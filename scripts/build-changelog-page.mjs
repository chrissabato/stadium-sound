// Generates docs/changelog.html from src/renderer/src/changelog.ts — the same
// data that drives the in-app What's New dialog. Run manually or via the
// release workflow's update-website-version job; never edit the output by hand.
//
//   node scripts/build-changelog-page.mjs
//
// Dependency-free on purpose: the workflow job that runs it has no npm ci.
import { readFileSync, writeFileSync } from 'fs'
import { dirname, join } from 'path'
import { fileURLToPath } from 'url'

const repo = join(dirname(fileURLToPath(import.meta.url)), '..')

const src = readFileSync(join(repo, 'src', 'renderer', 'src', 'changelog.ts'), 'utf8')
const arrayStart = src.indexOf('[', src.indexOf('export const CHANGELOG'))
if (arrayStart < 0) throw new Error('CHANGELOG array not found in changelog.ts')
// The file ends with the array literal, which is plain data (no code), so it
// can be evaluated directly once the TS wrapper is sliced off.
const releases = new Function(`return ${src.slice(arrayStart)}`)()

function esc(s) {
  return s.replaceAll('&', '&amp;').replaceAll('<', '&lt;').replaceAll('>', '&gt;')
}

function fmtDate(iso) {
  return new Date(`${iso}T00:00:00Z`).toLocaleDateString('en-US', {
    month: 'short', day: 'numeric', year: 'numeric', timeZone: 'UTC'
  })
}

